/** Same rules as the graph compiler — imported there so the two never drift. */
export const slugify = (s: string) =>
	s
		.toLowerCase()
		.trim()
		.replace(/[\s_]+/g, '-')
		.replace(/[^a-z0-9-]/g, '');

const esc = (s: string) =>
	s.replace(/[&<>"']/g, (c) => `&#${c.charCodeAt(0)};`);

/**
 * One chip. Record values are author-trusted constants; the only
 * content-derived string in the output is the escaped fallback label,
 * so the result is safe for innerHTML and Astro set:html.
 */
export function stackChipHtml(raw: string): string {
	const key = slugify(raw);
	const icon = STACK_ICONS[ALIASES[key] ?? key];
	if (!icon) return `<span class="stack-chip">${esc(raw)}</span>`;
	return `<span class="stack-chip"><svg viewBox="0 0 24 24" width="14" height="14" fill="${icon.hex}" aria-hidden="true"><path d="${icon.path}"/></svg>${esc(icon.label)}</span>`;
}

export const stackChipsHtml = (stack: string[]) =>
	`<span class="stack-chips">${stack.map(stackChipHtml).join('')}</span>`;

